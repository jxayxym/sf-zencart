<dt><h2>How to Preserve my Bridal Dress?</h2></dt>
<dd>
<h3>General attention</h3>
<p>Dresses should be stored in a room where the temperature and humidity levels
are constant, such as a guest room or spare closet. Never store your dress in
the attic or basement where there are extreme changes in temperature and/or
humidity. It is best to keep it in a dark (unexposed to sunlight), dry place,
folded or rolled in a clean white sheet. Hanging your dress on a hanger is not
recommended as it can cause stress to the fabric and create distortion. Do not
store your dress in plastic such as a garment bag. Plastic can emit gases that
will tend to yellow the fabric. If you decide to remove your dress from the one
place to another place, we suggest that cotton gloves should always be worn
when handling dresses.</p>
<h3>Yellowing</h3>
<p>It’s important to note that one of the leading causes of bridal dress yellowing
is the plastic bags that many brides keep their dresses in. Most plastics give
off damaging fumes that actually promote yellowing. But, even with proper care,
some fabrics will yellow more than others and it may be impossible to prevent
all yellowing.<br>
Generally, silk fabric yellows more than synthetic fabrics, such as polyester,
rayon and acetate. However, nylon, which is a synthetic, has a tendency to
yellow more than other synthetic fabrics. Dresses that can be wet cleaned have
an advantage, in that if they do yellow, they may be able to be whitened for
future use with a fabric whitener.<br>
Preserving your dress in an acid-free environment is your best protection
against yellowing. Padding your dress with acid-free tissue will help to
prevent acid migration. Buffered tissue should be used for dresses made of
synthetic fabrics such as polyester, rayon, and acetate. The buffering agents
in the buffered tissue gives added protection against acid migration. But
buffering agents may damage dresses made of animal proteins such as silk or
wool, therefore un-buffered, acid-free tissue is recommended for silk fabrics</p>

<h3>Permanent creasing</h3>
<p>
Flat storage is recommended for textiles and garments when possible. However,
because of the size and dimensions of wedding dresses, it is impractical. Some
compromise must be made, either by folding or hanging the dress. To help
prevent permanent creasing,.(Cotton gloves should always be worn when handling
preserved dresses.) Bagged dresses that are hung in a closet are better.</p>

<h3>Mildew and mold</h3>
<p>Keeping your dress in a breathe able environment will protect it best from
mildew and mold growth. When fabrics can breathe, the humidity level remains
constant around the garment as excess moisture dissipates into the air. But, if
moisture can condense inside a box or any container, then the dress is at risk
for mildew and mold growth.</p>

<h3>Oxidation spots</h3>
<p>An oxidation spot can occur when a substance that was not properly cleaned on
the dress oxidizes and turns brown. This can happen even if your dress has been
cleaned as dry-cleaning solvents do not remove all substances. Spills from
clear soda or wine may go unnoticed at the time of the initial cleaning. Unless
these spills are pretreated, it is likely they will oxidize over time.
Inspecting preserved dresses periodically ensures the dress remains in the best
condition. The sooner an oxidized stain is caught, the more likely it will be
able to be removed.</p>

<h3>Oxidation spots</h3>
<p>Keeping your dress covered will protect it from the damage caused by light and dust.</p>

<p><strong>　　一、信用卡电话支付</strong></p>
<p>　　如果你拥有一张信用卡，可以选择电话支付的方式，安全、方便、快捷，一个电话，轻松搞定。万表网客服中心通过了银行风险部门的信息安全检查要求，全程录像监控和电话录音，每位员工都签定了保密协议，不会保留和泄露客户的任何信息。<span style="color: #ff0000"></span></p>
<p><strong><img alt="" width="690" height="207" src="images/wbiao/14(27).jpg" data-pinit="registered"></strong></p>
<p><strong>　　二、中行信用卡分期付款</strong></p>
<p>　　中银系列信用卡个人卡持卡人以一次性支付或分期付款的方式，在万表网购买商品或服务，中国银行按照约定的手续费率扣除其应得手续费后，将持卡人支付的剩余款项划转万表网账户，之后持卡人按照约定的期数向中国银行偿还其透支款项。分期标准：分期付款的起分金额为1000元及以上商品。低于1000元的商品经中行认可后方可分期。</p>
<p><strong>　　需要提交的信用卡资料包括：</strong></p>
<p>　　1、信用卡卡号</p>
<p>　　2、信用卡的有效期</p>
<p>　　3、持卡人的身份证号码</p>
<p>　　4、持卡人姓名</p>
<p>　　5、分期期数(3期、6期、12期)</p>

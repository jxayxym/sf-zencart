<p><strong>　　三、网上支付</strong></p>
<p>　　网上支付，安全更快捷。万表网支持信用卡及借记卡在线支付的银行有：工商银行、招商银行、农业银行、建设银行、光大银行、中国银行、兴业银行、交通银行、广东发展银行、深圳发展银行、中国邮政储蓄银行、东亚银行、上海银行、北京银行等，如您在选择网上支付过程中有任何疑问，请拨打银行24小时客服热线或者万表网订购专线咨询，谢谢。</p>

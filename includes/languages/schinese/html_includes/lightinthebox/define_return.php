<div style="text-indent: 20.25pt"><span style="font-size: 12pt">10</span><span style="font-size: 12pt">天无条件退换货</span></div>
<div>&nbsp;</div>
<div style="text-indent: 20.25pt"><b>一般商品退换货政策</b></div>
<div style="text-indent: 20.25pt">自商品签收之日起，10日内可无条件申请退换货；30日内如有质量问题可申请退换货。</div>
<div style="text-indent: 20.25pt">&nbsp;</div>
<div style="text-indent: 20.25pt"><b>特殊商品退换货政策</b></div>
<div style="text-indent: 20.25pt">活动特价商品，如无质量问题，不支持退换货；自签收之日起30日内如有质量问题，支持退货、不支持换货（具体请详见活动页面的规则说明）。</div>
<div style="text-indent: 20.25pt">&nbsp;</div>
<div style="text-indent: 20.25pt"><b>详细说明</b></div>
<div style="text-indent: 20.25pt">1、非质量问题，请务必在商品签收之日起10日内申请退换货；质量问题，请务必在商品签收之日起30日内申请退换货，超过时限将不予办理。</div>
<div style="text-indent: 20.25pt">2、退换货之前，请致电客服确认退换货事宜。<span style="color: red">注：如事先未提交退换货申请</span>，将无法为您进行退换货办理。</div>
<div style="text-indent: 20.25pt">3、换货操作：A商品换B商品时，请将B商品编号及颜色在商品发货单中注明，不必申请退款，最后以B-A差额多退少补；若A是折扣产品，更换的B商品以网站售价为准，不与A商品享受同等的折扣。</div>
<div style="text-indent: 20.25pt">4、特别说明，以下情况不予办理退换货：</div>
<div style="text-indent: 20.25pt">1）任何非爱表网官方网站出售的商品。</div>
<div style="text-indent: 20.25pt">2）任何因非正常使用及保管导致出现质量问题的商品。</div>
<div style="text-indent: 20.25pt">3）超过10天无质量问题。</div>
<div style="text-indent: 20.25pt">4）商品与附件（赠品、吊牌、防尘袋、包装塑料纸、商品发货单、发票等）不完整。</div>
<div style="text-indent: 20.25pt">5）其它依法不应办理退换货的情况。</div>
<div style="text-indent: 20.25pt">&nbsp;</div>
<div style="text-indent: 20.25pt"><b>退换货费用承担标准</b>&nbsp;</div>
<div style="text-indent: 21pt">1、质量问题退换货，运费由爱表网承担；非质量问题退换货，运费由客户承担。</div>
<div style="text-indent: 21pt">2、运费承担标准：</div>
<div style="text-indent: 21pt">1）退回嘉兴仓：江浙沪地区8元，全国其他地区15元；</div>
<div style="text-indent: 21pt"><span style="text-indent: 21pt;">2）超出部分以实际发生金额为准。</span></div>
